package data;

import java.io.Serializable;

/**
 * The enum which contains Vehicle types.
 */
public enum VehicleType implements Serializable {
    /**
     * Car vehicle type.
     */
    CAR,
    /**
     * Plane vehicle type.
     */
    PLANE,
    /**
     * Drone vehicle type.
     */
    DRONE,
    /**
     * Ship vehicle type.
     */
    SHIP,
    /**
     * Chopper vehicle type.
     */
    CHOPPER;
}
