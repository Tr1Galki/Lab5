package data;

/**
 * The enum which contain Fuel types.
 *
 * @author Alever
 */
public enum FuelType {
    /**
     * Gasoline fuel type.
     */
    GASOLINE,
    /**
     * Kerosene fuel type.
     */
    KEROSENE,
    /**
     * Electricity fuel type.
     */
    ELECTRICITY,
    /**
     * Manpower fuel type.
     */
    MANPOWER,
    /**
     * Antimatter fuel type.
     */
    ANTIMATTER;
}
