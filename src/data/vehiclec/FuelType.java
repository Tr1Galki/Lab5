package data.vehiclec;

import java.io.Serializable;

/**
 * The enum which contain Fuel types.
 *
 * @author Alever
 */
public enum FuelType implements Serializable {
    /**
     * Gasoline fuel type.
     */
    GASOLINE,
    /**
     * Kerosene fuel type.
     */
    KEROSENE,
    /**
     * Electricity fuel type.
     */
    ELECTRICITY,
    /**
     * Manpower fuel type.
     */
    MANPOWER,
    /**
     * Antimatter fuel type.
     */
    ANTIMATTER;
}
